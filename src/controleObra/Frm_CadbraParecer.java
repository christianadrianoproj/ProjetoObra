    private void jbAddParecerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddParecerActionPerformed
        if (jtTitulo.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Entre com o tÃ­tulo da obra!");
            jtTitulo.requestFocus();
            return;
        }
        
        if (jtAutor.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Entre com o autor da obra!");
            jtAutor.requestFocus();
            return;
        }
         
        jpParecer.setVisible(true);
        limparTelaParecer();
        jbAddParecer.setEnabled(false);
        
        obra = new Obra();
        obra.setTitulo(jtTitulo.getText());
        obra.setAutor(jtAutor.getText());
        
    }//GEN-LAST:event_jbAddParecerActionPerformed

    private void jbAddPacerObraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAddPacerObraActionPerformed
        if (jtParecerista.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(null, "Entre com o parecerista!");
            jtParecerista.requestFocus();
            return;
        }
        
        if (jtParecer.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Entre com o parecer!");
            jtParecerista.requestFocus();
            return;
        }

        if (jtData.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Entre com a data!");
            jtData.requestFocus();
            return;
        }
        
        if (jtHora.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Entre com a hora!");
            jtHora.requestFocus();
            return;
        }
        
        SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat hf = new SimpleDateFormat("HH:mm:ss");
        
        Date dt;
        
        Parecer parecer = new Parecer();
        parecer.setParecerista(jtParecerista.getText());
        parecer.setConteudo(jtParecer.getText());
        
        try {
            dt = df.parse(jtData.getText());
        }
        catch (Exception e){
            dt = new Date();
        }
         
       parecer.setData(dt);
       
        try {
            dt = hf.parse(jtHora.getText());
        }
        catch (Exception e){
            dt = new Date();
        }
        
        parecer.setHora(dt);
        
        obra.adicionaParecer(parecer);
        
        limparTelaParecer();
    }//GEN-LAST:event_jbAddPacerObraActionPerformed

